package projetoMod1;

public class PostosConstructor {
	
	String nome;
	String região;
	String Endereço;
	int Estoque;
	int Capacidade;
	
	

	public PostosConstructor(String nome,	String região, String endereço, int estoque, int capacidade) {
		
		this.nome = nome;
		this.região = região;
		this.Endereço = endereço;
		this.Estoque = estoque;
		this.Capacidade = capacidade;
		
		
	}
	

}
